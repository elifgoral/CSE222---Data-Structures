public interface UserInterface {
	/**
	 * That method sets the parameter name to the user's name.
	 * @param Name name which will be assign to User's name.
	 */
	void setName(String Name);
	/**
	 * That method returns user's name.
	 * @return user's name
	 */
	String getName();
	/**
	 * That method sets the parameter surname to the user's surname.
	 * @param Surname surname which will be assign to User's surname.
	 */
	void setSurname(String Surname);
	
	/**
	 * That method returns user's surname.
	 * @return user's surname
	 */
	String getSurname();		
}
